import { readFileSync, writeFileSync, existsSync } from "node:fs";

const envFile = ".env.local";
const outputFile = "supabase-config.js";
const requiredKeys = ["NEXT_PUBLIC_SUPABASE_URL", "NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY"];

function parseEnv(contents) {
  return Object.fromEntries(
    contents
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith("#"))
      .map((line) => {
        const separator = line.indexOf("=");
        const key = separator >= 0 ? line.slice(0, separator).trim() : line;
        let value = separator >= 0 ? line.slice(separator + 1).trim() : "";
        value = value.replace(/^['"]|['"]$/g, "");
        return [key, value];
      })
  );
}

const env = existsSync(envFile) ? parseEnv(readFileSync(envFile, "utf8")) : {};
const publicConfig = Object.fromEntries(requiredKeys.map((key) => [key, env[key] || process.env[key] || ""]));
const missingKeys = requiredKeys.filter((key) => !publicConfig[key]);

writeFileSync(
  outputFile,
  `// Generated by npm run supabase:config. Do not commit local environment values.\nwindow.SUPABASE_CONFIG = ${JSON.stringify(publicConfig, null, 2)};\n`,
  "utf8"
);

if (missingKeys.length) {
  console.warn(`Generated ${outputFile}, but missing: ${missingKeys.join(", ")}`);
  process.exitCode = 1;
} else {
  console.log(`Generated ${outputFile} from ${envFile}.`);
}
